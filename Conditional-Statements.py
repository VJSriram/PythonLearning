x = 42
y = 42

#if statement is one block 
if x > y: 
    print('x < y: x is {} and y is {}'.format(x, y))
    
elif x < y: # else if is elif in python 
    print('x < y: x is {} and y is {}'.format(x, y))
    
elif x == y: # else if is elif in python 
    print('x < y: x is {} and y is {}'.format(x, y))    
    
else:
    print('do something else')